import type { Country, RegionId, Profile, FoodProfile } from '../types';
import { regions } from './regions';

// 헬퍼: 지역 기본값으로 국가 생성
function createCountry(
  id: string,
  name: string,
  nameEn: string,
  emoji: string,
  region: RegionId,
  description: string,
  detail: string,
  highlights: [string, string, string],
  profileOverrides?: Partial<Profile>,
  foodOverrides?: Partial<FoodProfile>,
  climate?: string,
  language?: string
): Country {
  const regionData = regions[region];
  return {
    id,
    name,
    nameEn,
    emoji,
    region,
    profile: { ...regionData.profile, ...profileOverrides } as Profile,
    food: { ...regionData.food, ...foodOverrides } as FoodProfile,
    climate: climate ?? regionData.climate,
    language: language ?? 'en',
    description,
    detail,
    highlights
  };
}

